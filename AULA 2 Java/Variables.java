public class Variables {
    public static void main(String args[])
    {
        // Declare variables
        int num1;
        double num2;
        String name;
        char initial;
        boolean check;

        // Initialize variables
        num1 = 7;
        num2 = 3.5;
        name = "Augusto";
        initial = 'A';
        check = true;

        // Print variables
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(name);
        System.out.println(initial);
        System.out.println(check);
    }
}